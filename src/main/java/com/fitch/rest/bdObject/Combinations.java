package com.fitch.rest.bdObject;

import java.util.List;

public class Combinations {

	private String inputNumber;
	private String combinationsNumber;
	private List<String> combinations;
	
	public String getCombinationsNumber() {
		return combinationsNumber;
	}
	public void setCombinationsNumber(String combinationsNumber) {
		this.combinationsNumber = combinationsNumber;
	}
	public List<String> getCombinations() {
		return combinations;
	}
	public void setCombinations(List<String> combinations) {
		this.combinations = combinations;
	}
	public String getInputNumber() {
		return inputNumber;
	}
	public void setInputNumber(String inputNumber) {
		this.inputNumber = inputNumber;
	}
	
}
